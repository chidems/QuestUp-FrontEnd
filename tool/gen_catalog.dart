void _emitSprites(StringBuffer out, String listName, String dir, String kind) {
  out.writeln('const List<SpriteAsset> $listName = [');
  for (final f in _pngs(dir)) {
    final base = _baseName(f);
    if (kind == 'eyes' && _excludedEyes.contains(base)) continue;
    final (w, h) = _pngSize(f);
    out.writeln("  SpriteAsset('${kind}_$base', '${_humanize(base)}', "
        "'${_assetPath(f)}', $w, $h),");
  }
  out.writeln('];\n');
}

void _emitHair(StringBuffer out) {
  out.writeln('const List<HairAsset> kHairStyles = [');
  for (final f in _pngs('sprites/hair')) {
    final base = _baseName(f);
    final n = int.parse(base.split('_').last);
    final (w, h) = _pngSize(f);
    out.writeln("  HairAsset('$base', 'Style $n', '${_assetPath(f)}', "
        "$w, $h, '${_hairColor(n)}'),");
  }
  out.writeln('];\n');
}

void _emitClothes(StringBuffer out) {
  out.writeln('const List<ClothingAsset> kClothes = [');
  for (final style in _styles.keys) {
    for (final slotDir in ['tops', 'bottoms']) {
      final slot = slotDir == 'tops' ? 'top' : 'bottom';
      for (final rarityDir in ['common', 'uncommon', 'rare_epic']) {
        final dir = 'sprites/clothes/$style/$slotDir/$rarityDir';
        for (final f in _pngs(dir)) {
          final base = _baseName(f);
          final n = int.parse(base.split('_').last);
          // rare_epic folders alternate deterministically: odd=rare, even=epic.
          final rarity = rarityDir == 'rare_epic'
              ? (n.isOdd ? 'rare' : 'epic')
              : rarityDir;
          final id = '${style}_${slot}_$base';
          final label = _styles[style]!;
          final name =
              '$label ${slot == 'top' ? 'Top' : 'Bottom'} ${n.toString().padLeft(2, '0')}';
          final (w, h) = _pngSize(f);
          out.writeln(
              "  ClothingAsset('$id', '$name', '${_assetPath(f)}', $w, $h, "
              "'$slot', '$style', '$rarity'),");
        }
      }
    }
  }
  out.writeln('];\n');
}

void _emitItems(StringBuffer out) {
  final rarities = _itemRarities.split(RegExp(r'\s+'));
  final files = _pngs('sprites/items');
  if (files.length != _itemNames.length || rarities.length != files.length) {
    throw StateError('items: ${files.length} files, ${_itemNames.length} '
        'names, ${rarities.length} rarities — counts must match');
  }
  out.writeln('const List<ItemAsset> kItems = [');
  for (var i = 0; i < files.length; i++) {
    final f = files[i];
    final rarity = _rarityNames[rarities[i]]!;
    final price = _rarityPrices[rarity]!;
    final (w, h) = _pngSize(f);
    final name = _itemNames[i].replaceAll("'", r"\'");
    out.writeln("  ItemAsset('${_baseName(f)}', '$name', '${_assetPath(f)}', "
        "$w, $h, '$rarity', $price),");
  }
  out.writeln('];');
}

List<File> _pngs(String dir) {
  final files = Directory(dir)
      .listSync()
      .whereType<File>()
      .where((f) => f.path.endsWith('.png'))
      .toList()
    ..sort((a, b) => a.path.compareTo(b.path));
  if (files.isEmpty) throw StateError('no PNGs found in $dir');
  return files;
}

String _assetPath(File f) => f.path.replaceAll(r'\', '/');

String _baseName(File f) =>
    f.uri.pathSegments.last.replaceAll('.png', '');

String _humanize(String s) => s
    .split('_')
    .map((w) => '${w[0].toUpperCase()}${w.substring(1)}')
    .join(' ');

/// Reads width/height from the PNG IHDR chunk (bytes 16..23, big-endian).
(int, int) _pngSize(File f) {
  final b = f.openSync().readSync(24);
  int be(int o) => (b[o] << 24) | (b[o + 1] << 16) | (b[o + 2] << 8) | b[o + 3];
  return (be(16), be(20));
}
