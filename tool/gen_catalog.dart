/// A drawable sprite with its intrinsic pixel size (used for layer anchoring).
class SpriteAsset {
  final String id;
  final String name;
  final String asset;
  final int w;
  final int h;

  const SpriteAsset(this.id, this.name, this.asset, this.w, this.h);
}

class HairAsset extends SpriteAsset {
