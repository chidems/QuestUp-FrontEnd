/// Dropped from the catalog: too close in color to a kept eye sprite
/// (aqua/teal cover the cyans, blue covers royal_blue, purple covers violet,
/// pink/magenta cover rose). Files stay on disk but aren't offered.
const _excludedEyes = {
  'cyan', 'turquoise', 'ice_blue', 'royal_blue', 'violet', 'rose',
};

/// The 112 hair sprites are 8 styles x 14 colors, cycling every 14 files.
/// Verified against _qa_hair.png + per-sprite average HSL. Offsets 7/8 vary
/// by style (gray/silver in early styles, brown/blonde in later ones), so
/// those use the measured split.
String _hairColor(int n) {
  final offset = (n - 1) % 14 + 1;
  switch (offset) {
    case 1:
      return 'black';
    case 2:
    case 3:
      return 'brown';
    case 4:
    case 5:
      return 'blonde';
    case 6:
    case 14:
      return 'red';
    case 7:
      return n == 7 ? 'silver' : 'brown';
    case 8:
      return const {78, 92, 106}.contains(n) ? 'blonde' : 'silver';
    case 9:
      return 'purple';
    case 10:
      return 'blue';
    case 11:
      return 'green';
    case 12:
      return 'pink';
    default:
      return 'orange';
  }
}

